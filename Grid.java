    public Grid(int w, int l)
    {
        grid = new Position[w][l];
        for(int i = 0; i < w; i++)
        {
            for(int j = 0; j < l; j++)
            {
                grid[w][l] = new Position(i * positionSize, j * positionSize, positionSize, positionSize, i , j);
            }
        }
    }
    
    public Position getPosition(Point p)
    {
        return grid[(int)p.getX() / positionSize][(int)p.getY() / positionSize];
    }
    
    public int getSize()
    {
        return positionSize;
    }
    
    public void setFilled(Position p, Entity e)
    {
        if(e == null)
            p.setType(0);
        else if(e instanceof Turret)
            p.setType(1);
        else if(e instanceof Enemy)
            p.setType(2);
        else if(e instanceof Bullet)
            p.setType(3);
    }
    
    public boolean isValidPosition(Position p)
    {
       int x = p.getRow(), y = p.getCol();
       if(x == 0 || y == 0 || x == 1 || y == 1)
          return false;
       else
       {
           for(int i = x; i > x - 3; i--)
           {
               for(int j = y; j > y - 3; j--)
               {
                   if(!(grid[i][j].isEmpty()) || isInPath(grid[i][j]))
                       return false;
               }
           }
       }
       return true;
    }
    
    public boolean isEmpty(Position p)
    {
        return p.isEmpty();
    }
    
    public boolean isInPath(Position p)
    {
        return path[p.getRow()][p.getCol()] == 1;
    }
    
    public boolean isInPath(int i, int j)
    {
        return true;
    }
    
    public void remove(Position p)
    {
        p.setType(0);
    }
    
    public Position[][] getGameGrid()
    {
        return grid;
    }
    
    public void draw(Graphics g)
    {
        for(Position[] p : grid)
            for(Position pos : p)
                pos.draw(g);
    }
